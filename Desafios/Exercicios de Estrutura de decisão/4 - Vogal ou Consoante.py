#Faça um Programa que verifique se uma letra digitada é vogal ou consoante.
x1 = str(input("Digite uma letra "))
if x1 == 'a' or x1 == 'A' or x1 == 'e' or x1 == 'E' or x1 == 'i' or x1 == 'I' or x1 == 'O' or x1 == 'o' or x1 == 'u' or x1 == 'U':
    print("Vogal")
else:
    print("Consoante")

