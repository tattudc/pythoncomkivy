#Operadores Lógicos
#True ou False
print(7 != 7 and 3 == 3) #no AND ambos tem de ser True para a afirmação ser True
print(3 == 3 and 5 != 2)
print(3 == 3 or 5 == 2) #no OR ou uma das duas tem de ser True para afirmação ser True
print(3 == 2 or 2 == 1)
print(not 3 == 3) #no NOT ele nega a afirmação, se True fica False e vice e versa
print(not 3 != 3)

