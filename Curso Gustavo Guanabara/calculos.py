n1= float(input('Digite um número '))
n2= float(input('Digite outro número '))
result= n1+n2
result2= n1-n2
result3= n1*n2
result4= n1/n2
result5= n1**n2
result6= n1//n2
result7= n1%n2

print ('A soma de {} + {} é {}'.format(n1,n2,result))
print ('A subtração de {} - {} é {}'.format(n1,n2,result2))
print ('A multiplicação de {} * {} é {}'.format(n1,n2,result3))
print ('A divisão de {} / {} é {}'.format(n1,n2,result4))
print ('A Exponenciação de {} ** {} é {}'.format(n1,n2,result5))
print ('O modulo de {} // {} é {}'.format(n1,n2,result6))
print ('O resto de {} % {} é {}'.format(n1,n2,result7))